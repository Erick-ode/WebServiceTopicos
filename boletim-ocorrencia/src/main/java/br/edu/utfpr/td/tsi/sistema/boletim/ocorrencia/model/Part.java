package br.edu.utfpr.td.tsi.sistema.boletim.ocorrencia.model;

public class Part {

    private String name;
    private String email;
    private String phone;
    private String typeInvolvement;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getTypeInvolvement() {
        return typeInvolvement;
    }

    public void setTypeInvolvement(String typeInvolvement) {
        this.typeInvolvement = typeInvolvement;
    }
}
